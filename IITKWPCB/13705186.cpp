#include<bits/stdc++.h>
using namespace std;
typedef long long int LL;
int main()
{
    int t;
    scanf("%d",&t);
    while(t--)
    {
        LL n;
        scanf("%lld",&n);
        for(LL i=n/2;i>=1;i--)
        {
            if(__gcd(i,n)==1)
            {
                printf("%lld\n",i);
                break;
            }
        }
    }
    return 0;
}
